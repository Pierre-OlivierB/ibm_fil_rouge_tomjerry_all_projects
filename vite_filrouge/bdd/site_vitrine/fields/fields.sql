-- !-------------------------------------------------------------
-- !---------Surface pour pomme de terre-------------------------
-- !-------------------------------------------------------------

SELECT SUM(fs.set_delivered)
FROM
    field as f,
    type_field as tf,
    cult_give as cg,
    is_cult_affilied as ica,
    culture as c,
    field_set as fs
WHERE
    f.id_type_field = tf.id_type_field
    AND f.num_field = ica.Num_Field
    AND ica.Id_culture = 5
    AND cg.Id_culture = 5
    AND c.Id_culture = 5
    AND ica.date_cult_affilied = cg.date_cult
    AND fs.Num_field = f.num_field
    AND fs.Id_Unity = 5;

-- *----------------------------Better
SELECT ue.label_under_earth as label, SUM(fs.set_delivered) as data
FROM
    field as f,
    type_field as tf,
    cult_give as cg,
    is_cult_affilied as ica,
    culture as c,
    field_set as fs,
    under_earth as ue
WHERE
    f.id_type_field = tf.id_type_field
    AND f.num_field = ica.Num_Field
    AND ica.Id_culture = 5
    AND cg.Id_culture = 5
    AND c.Id_culture = 5
    AND ue.Id_culture = 5
    AND ica.date_cult_affilied = cg.date_cult
    AND fs.Num_field = f.num_field
    AND fs.Id_Unity = 5;